    private void accept_buttonActionPerformed(java.awt.event.ActionEvent evt) {                                         

		Atom c2 = new Atom();
		c2.symbol = "C";
		c2.set_position_x(0);
		c2.set_position_y(0);
		Molecule meth2 =  new Molecule(c2);
		meth2.set_height(3);
		meth2.set_width(3);
		Atom hh1 = new Atom();
		hh1.symbol = "H";
		hh1.set_position_x(1);
		hh1.set_position_y(0);
		Bond bbeth1 = new Bond(hh1, 1, c2, 1);
		bbeth1.next = hh1;
		c2.add_bond(bbeth1);
		Atom hh2 = new Atom();
		hh2.symbol = "H";
		hh2.set_position_x(0);
		hh2.set_position_y(-1);
		Bond bbeth2 = new Bond(hh2, 2, c2, 1);
		bbeth2.next = hh2;
		c2.add_bond(bbeth2);
		Atom hh3 = new Atom();
		hh3.symbol = "H";
		hh3.set_position_x(-1);
		hh3.set_position_y(0);
		Bond bbeth3 = new Bond(hh3, 3, c2, 1);
		bbeth3.next = hh3;
		c2.add_bond(bbeth3);
		Atom hh4 = new Atom();
		hh4.symbol = "H";
		hh4.set_position_x(0);
		hh4.set_position_y(1);
		Bond bbeth4 = new Bond(hh4, 0, c2, 1);
		bbeth4.next = hh4;
		c2.add_bond(bbeth4);
		
		m = drawing_area.get_molecule();

		System.out.println(bbeth4.type);
		if (Compare_Molecules.rotation_isomer(m, meth2)) {
			System.out.println("rotation true");
			printing_area.correct =1;
		} else {
			System.out.println("rotation false");
			printing_area.correct = -1;
		}
    	printing_area.print_molecule(m);
    }   


    private void try_next_buttonActionPerformed(java.awt.event.ActionEvent evt) {                                             	
    	printing_area.correct = 0;
    	drawing_area.get_stroke_list().getStrokes().removeLast();
    	printing_area.clear_molecule();
    	drawing_area.set_stroke_list(drawing_area.get_stroke_list());
    	drawing_area.repaint();
    	int height = -1;
		try {
			m = Synthesize_Molecule.synthesize(drawing_area.get_stroke_list());
			
	    	int[] dimensions = m.set_dimensions();

	    	height = m.get_height();
	    	int width = m.get_width();
	    	
	    	printing_area.print_molecule(m);
		} catch (NumberFormatException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}
		
    }                                        

