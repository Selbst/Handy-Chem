    private void accept_buttonActionPerformed(java.awt.event.ActionEvent evt) throws IOException {                                         
    	if (current_molecule != -1 && current_molecule < num_samples) {
    		CopyFile.copyfile("testing/molecule_samples/raw_samples/" + current_molecule + ".mol", "testing/molecule_samples/correct/" + current_molecule + ".mol");
    	}
    	

    	
    	
    	do_next();
    }                                        

    private void try_next_buttonActionPerformed(java.awt.event.ActionEvent evt) throws IOException {                                         
    	if (current_molecule != -1 && current_molecule < num_samples) {
    		CopyFile.copyfile("testing/molecule_samples/raw_samples/" + current_molecule + ".mol", "testing/molecule_samples/incorrect_structure/" + current_molecule + ".mol");
    	}
    	do_next();
    }
    
    public void do_next() throws IOException {
    	if (current_molecule < num_samples) {
        	current_molecule++;
        	drawing_area.set_stroke_list(ReadTrainingCharacters.getCharsFromFile("testing/molecule_samples/raw_samples/" + current_molecule + ".mol").getFirst());
        	Molecule m;
    		try {
    			m = Synthesize_Molecule.synthesize(drawing_area.get_stroke_list());
    	    	int[] dimensions = m.set_dimensions();

    	    	int height = m.get_height();
    	    	int width = m.get_width();
    	    	
    	    	
    	    	printing_area.print_molecule(m);
    		} catch (NumberFormatException e) {
    			e.printStackTrace();
    		} catch (IOException e) {
    			e.printStackTrace();
    		}  
    	}
    }

